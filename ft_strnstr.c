#include "libft.h"

char	*ft_strnstr(const char *s1, const char *s2, size_t len)
{
	size_t	i;
	size_t	j;

	i = 0;
	if (!s2)
		return (s1);
	while (s1[i])
	{
		j = 0;
		if (s1[i] == s2[j])
		{
			while (s1[i + j] == s2[j])
			{
				if (s2[j + 1] == '\0')
					return (s1 + i);
				j++;
			}
		}
		i++;
	}
	return (0);
}
